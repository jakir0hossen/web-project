                    <h4>লিডিং ইউনিভার্সিটির শিক্ষার্থীর ইরাসমাস মুন্ডাস স্কলারশিপ অর্জন
                    </h4>
                    <p>লিডিং ইউনিভার্সিটির শিক্ষার্থীর ইরাসমাস মুন্ডাস স্কলারশিপ অর্জন
লিডিং ইউনিভার্সিটির শিক্ষার্থী ইরাসমাস মুন্ডাস স্কলারশিপ অর্জন করেছেন।বিশ্ববিদ্যালয়ের ইলেকট্রিক্যাল এবং ইলেকট্রনিক ইঞ্জিনিয়ারিং (ইইই) বিভাগের ২৩তম ব্যাচের ছাত্র তারেক আনোয়ার শিকদার ইরাসমাস মুন্ডাস জয়েন্ট মাস্টার স্কলারশিপ অর্জন করেছেন। এই সম্মানজনক স্কলারশিপের মাধ্যমে তিনি ইউরোপের তিনটি শীর্ষস্থানীয় বিশ্ববিদ্যালয়- স্পেনের ইউনিভার্সিটি অব দ্য বাস্ক কান্ট্রি (UPV/EHU), গ্রীসের ইন্টারন্যাশনাল হেলেনিক ইউনিভার্সিটি (IHU), এবং ফিনল্যান্ডের ইউনিভার্সিটি অব ভাসা (UWASA) থেকে স্মার্ট সিটিস অ্যান্ড কমিউনিটিস (SMACCs) প্রোগ্রামে জয়েন্ট মাস্টার্স ডিগ্রি অর্জন করবেন। এই প্রোগ্রামটি স্মার্ট সিটি উন্নয়নের বিভিন্ন ক্ষেত্রে যেমন: নগর পরিকল্পনা, শক্তি ব্যবস্থাপনা, এবং তথ্য প্রযুক্তি বিষয়ে আন্তঃবিভাগীয় শিক্ষা এবং প্রশিক্ষণ প্রদান করে।
তারেকের এই অসামান্য সাফল্যে লিডিং ইউনিভার্সিটির প্রতিষ্ঠাতা ও ট্রাস্টি বোর্ডের চেয়ারম্যান দানবীর ড. সৈয়দ রাগীব আলী এবং ইইই বিভাগের বিভাগীয় প্রধান রফিকুল ইসলাম তাকে অভিনন্দন জানিয়ে বলেন, তারেকের এই সাফল্য আমাদের বিশ্ববিদ্যালয়ের সুনাম বৃদ্ধি করেছে এবং আমাদের ছাত্র-ছাত্রীদের জন্য এক নতুন দিগন্তের উন্মোচন করেছে। তাঁরা এই শিক্ষার্থীর ভবিষ্যৎ জীবন ও ক্যারিয়ারের জন্য শুভকামনা জ্ঞাপন করেন। 
ইইই বিভাগের বিভাগীয় প্রধান আরও বলেন, এই স্কলারশিপ তারেকের মেধা এবং ইইই বিভাগের উচ্চমানের শিক্ষার প্রতিফলন। তারেক এই সুযোগ কাজে লাগিয়ে আন্তর্জাতিক অঙ্গনে লিডিং ইউনিভার্সিটি এবং বাংলাদেশের নাম উজ্জ্বল করবে বলে আশাবাদ প্রকাশ করেন তিনি।
ইরাসমাস মুন্ডাস প্রোগ্রামটি সম্পূর্ণ অর্থায়িত, যা তারেককে পূর্ণ টিউশন ফি, মাসিক ভাতা এবং আন্তর্জাতিক অভিজ্ঞতা লাভের সুযোগ প্রদান করবে। এই প্রোগ্রামের মাধ্যমে তারেক আন্তর্জাতিক পর্যায়ে বিভিন্ন দেশের শিক্ষার্থীদের সাথে একাডেমিক এবং সাংস্কৃতিক অভিজ্ঞতা বিনিময় করার সুযোগ পাবেন, যা তার ব্যক্তিগত এবং পেশাগত জীবনে অসাধারণ ভূমিকা পালন করবে।</p>
